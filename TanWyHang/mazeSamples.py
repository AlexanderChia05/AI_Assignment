# print all coordinate with 0s in (row, column) format
def get_coordinates(maze):
    coordinates = []
    for row in range(len(maze)):
        for col in range(len(maze[row])):
            if maze[row][col] == 0:
                coordinates.append((row, col))
    return coordinates
